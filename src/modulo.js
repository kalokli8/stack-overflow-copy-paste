export default modulo;

/**
 * This method will get modulo (remainder) from two numbers
 *
 * @param {Number} num1 - first number
 * @param {Number} num2 - second number
 * @return {Number} - Result of modulo of first number divivied by second number
 */
function modulo(num1, num2) {
  return num1 % num2;
}
